/* Table creation */
DROP TABLE IF EXISTS customer;
DROP TABLE IF EXISTS loan;
DROP TABLE IF EXISTS borrower;

CREATE TABLE customer( 
    name   VARCHAR(100) PRIMARY KEY,  
    credit INTEGER
);
CREATE TABLE loan( 
    no   VARCHAR(100) PRIMARY KEY,  
    type VARCHAR(100),  
    minCredit INTEGER
);

CREATE TABLE borrower( 
    cname VARCHAR(100),
    lno VARCHAR(100),
    due DATE,
    FOREIGN KEY(cname) REFERENCES customer(name) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY(lno) REFERENCES loan(no) ON DELETE CASCADE ON UPDATE CASCADE
);

